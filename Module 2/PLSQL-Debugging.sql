-- 4. PROCEDURE: adjust_salaries_by_commission


CREATE OR REPLACE PROCEDURE adjust_salaries_by_commission IS

    CURSOR employee_cursor IS
        SELECT
            employee_id,
            salary,
            commission_pct
        FROM employees;

    new_salary NUMBER;

BEGIN

    debug_utils.log_message(
        'adjust_salaries_by_commission',
        1,
        'Procedure started'
    );

    FOR employee_record IN employee_cursor LOOP

        debug_utils.log_variable(
            'employee_id',
            TO_CHAR(employee_record.employee_id)
        );

        debug_utils.log_variable(
            'commission_pct',
            CASE
                WHEN employee_record.commission_pct IS NULL THEN 'NULL'
                ELSE TO_CHAR(employee_record.commission_pct)
            END
        );

        IF employee_record.commission_pct IS NOT NULL THEN

            -- Commission available:
            -- increase salary proportionally
            new_salary :=
                employee_record.salary *
                (1 + employee_record.commission_pct);

            debug_utils.log_message(
                'adjust_salaries_by_commission',
                10,
                'Salary increased based on commission percentage'
            );

        ELSE

            -- No commission:
            -- apply a default 2% increase
            new_salary :=
                employee_record.salary * 1.02;

            debug_utils.log_message(
                'adjust_salaries_by_commission',
                11,
                'Salary increased by the default 2%'
            );

        END IF;

        UPDATE employees
        SET salary = new_salary
        WHERE employee_id = employee_record.employee_id;

        debug_utils.log_variable(
            'new_salary',
            TO_CHAR(new_salary)
        );

        debug_utils.log_message(
            'adjust_salaries_by_commission',
            12,
            'Salary successfully updated'
        );

    END LOOP;

    debug_utils.log_message(
        'adjust_salaries_by_commission',
        99,
        'Procedure completed'
    );

EXCEPTION
    WHEN OTHERS THEN

        -- Log the error and re-raise it
        debug_utils.log_error(
            'adjust_salaries_by_commission',
            SQLERRM
        );

        RAISE;

END;
/

-- 5. EXECUTE THE PROCEDURE AND VERIFY THE RESULTS


BEGIN

    debug_utils.enable_debug;

    adjust_salaries_by_commission;

    debug_utils.disable_debug;

END;
