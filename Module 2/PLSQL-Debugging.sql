-- 1. TABLE USED TO STORE ALL DEBUGGING MESSAGES


CREATE TABLE debug_log
(
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    log_time TIMESTAMP DEFAULT SYSTIMESTAMP,

    -- Name of the procedure or module that generated the message
    module_name VARCHAR2(100),

    -- Line number in the source code (entered manually)
    line_no NUMBER,

    -- Debug message
    log_message VARCHAR2(4000),

    -- Oracle session ID where the application is running
    session_id VARCHAR2(50)
        DEFAULT SYS_CONTEXT('USERENV', 'SESSIONID')
);


-- 2. DEBUG PACKAGE: debug_utils


CREATE OR REPLACE PACKAGE debug_utils IS

    -- When FALSE, no messages are written into debug_log
    g_debug_mode BOOLEAN := FALSE;

    -- Enable / Disable debugging
    PROCEDURE enable_debug;
    PROCEDURE disable_debug;

    -- Logging procedures
    PROCEDURE log_message(
        p_module_name VARCHAR2,
        p_line_number NUMBER,
        p_message VARCHAR2
    );

    PROCEDURE log_variable(
        p_variable_name VARCHAR2,
        p_variable_value VARCHAR2
    );

    PROCEDURE log_error(
        p_procedure_name VARCHAR2,
        p_error_message VARCHAR2
    );

END debug_utils;
/

CREATE OR REPLACE PACKAGE BODY debug_utils IS

    -- Enable debugging
    PROCEDURE enable_debug IS
    BEGIN
        g_debug_mode := TRUE;
    END;

    -- Disable debugging
    PROCEDURE disable_debug IS
    BEGIN
        g_debug_mode := FALSE;
    END;

    -- Log a standard message
    PROCEDURE log_message(
        p_module_name VARCHAR2,
        p_line_number NUMBER,
        p_message VARCHAR2
    ) IS
    BEGIN
        -- Write only if debugging is enabled
        IF g_debug_mode THEN
            INSERT INTO debug_log
                (module_name, line_no, log_message)
            VALUES
                (p_module_name, p_line_number, p_message);
        END IF;
    END;

    -- Log a variable value
    PROCEDURE log_variable(
        p_variable_name VARCHAR2,
        p_variable_value VARCHAR2
    ) IS
    BEGIN
        IF g_debug_mode THEN
            INSERT INTO debug_log
                (module_name, line_no, log_message)
            VALUES
                (
                    'VARIABLE_LOG',
                    0,
                    'Variable ' || p_variable_name || ' = ' || p_variable_value
                );
        END IF;
    END;

    -- Log an error message
    PROCEDURE log_error(
        p_procedure_name VARCHAR2,
        p_error_message VARCHAR2
    ) IS
    BEGIN
        IF g_debug_mode THEN
            -- line_no = -1 indicates an error
            INSERT INTO debug_log
                (module_name, line_no, log_message)
            VALUES
                (
                    p_procedure_name,
                    -1,
                    'ERROR: ' || p_error_message
                );
        END IF;
    END;

END debug_utils;
/


